WITH RECURSIVE TIME24 AS 
(
    SELECT
        0 AS HOUR
    
    UNION ALL
    
    SELECT
        1 + HOUR
    FROM
        TIME24
    WHERE
        HOUR < 23
) 

SELECT
    TIME24.HOUR
    ,COUNT(AO.DATETIME) AS COUNT
FROM
    TIME24
LEFT JOIN
    ANIMAL_OUTS AS AO
ON
    TIME24.HOUR = HOUR(AO.DATETIME)
GROUP BY
    TIME24.HOUR
ORDER BY
    TIME24.HOUR ASC;